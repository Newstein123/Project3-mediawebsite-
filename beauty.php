        <h1>Where You Get Them</h1>
        <p class="p-5">You won’t get stretch marks on your face, hands, or feet, but they can be almost anywhere else. They’re most likely to be where your body stores fat, like your belly, hips, thighs, breasts, and buttocks. You also might notice them on your lower back or the backs of your arms. Genetics may play a role in stretch marks. For example, if your mother got them on their thighs during pregnancy, you may be more likely to have them there.</p><br><br>
